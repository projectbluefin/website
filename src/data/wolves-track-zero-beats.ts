function nearestBeatIndex(time: number): number {
  const beats = TRACK_ZERO_BEAT_TIMES
  let lo = 0
  let hi = beats.length - 1
  while (lo < hi) {
    const mid = (lo + hi) >> 1
    if (beats[mid] < time) {
      lo = mid + 1
    }
    else {
      hi = mid
    }
  }
  if (lo > 0 && Math.abs(beats[lo - 1] - time) <= Math.abs(beats[lo] - time)) {
    return lo - 1
  }
  return lo
}

/**
 * Allocate `count` slide end times across the measured beats between
 * `startTime` and `endTime`.
 *
 * `tiers` is a descending list of per-slide beat counts (e.g. [16, 8]).
 * Every slide starts at the smallest tier; earlier slides are upgraded to
 * larger tiers while beats remain, and the first slide absorbs any
 * remainder. This yields long holds at the front of a section that
 * tighten toward its end, with every cut landing on a measured beat.
 *
 * The final cut is clamped to `endTime` exactly so sections chain without
 * drift (and so hard anchors like the 167.8s hero lock take precedence).
 * If the pool is too large for the beat budget the allocator falls back
 * to a uniform division of the window.
 */
export function trackZeroBeatCuts(
  startTime: number,
  endTime: number,
  count: number,
  tiers: readonly number[],
): number[] {
  if (count <= 0) {
    return []
  }
  const beats = TRACK_ZERO_BEAT_TIMES
  const startIndex = nearestBeatIndex(startTime)
  const endIndex = nearestBeatIndex(endTime)
  const totalBeats = endIndex - startIndex
  const base = tiers[tiers.length - 1]
  if (totalBeats < base * count) {
    const uniform = (endTime - startTime) / count
    return Array.from({ length: count }, (_, i) =>
      i === count - 1 ? endTime : startTime + uniform * (i + 1))
  }
  const perSlide = Array.from({ length: count }, () => base)
  let remaining = totalBeats - base * count
  for (const tier of tiers.slice(0, -1)) {
    for (let i = 0; i < count; i++) {
      const upgrade = tier - perSlide[i]
      if (upgrade > 0 && remaining >= upgrade) {
        perSlide[i] = tier
        remaining -= upgrade
      }
    }
  }
  perSlide[0] += remaining
  const cuts: number[] = []
  let index = startIndex
  for (const beatCount of perSlide) {
    index += beatCount
    cuts.push(beats[index])
  }
  cuts[count - 1] = endTime
  return cuts
}

/**
 * Allocates measured cuts around an explicit tempo pickup. Slides before the
 * pickup use the slower beat tier; slides after it use the faster tier, with a
 * cut snapped exactly to the pickup beat when the schedule crosses it.
 */
export function trackZeroBeatCutsWithPickup(
  startTime: number,
  pickupTime: number,
  endTime: number,
  count: number,
  beforeBeats: number,
  afterBeats: number,
): number[] {
  if (count <= 0) {
    return []
  }

  const startIndex = nearestBeatIndex(startTime)
  const pickupIndex = nearestBeatIndex(pickupTime)
  const endIndex = nearestBeatIndex(endTime)
  const totalBeats = endIndex - startIndex
  if (totalBeats < afterBeats * count) {
    return Array.from({ length: count }, (_, index) =>
      index === count - 1 ? endTime : startTime + ((endTime - startTime) * (index + 1)) / count)
  }

  const cuts: number[] = []
  let index = startIndex
  for (let slide = 0; slide < count; slide++) {
    const remainingSlides = count - slide - 1
    const targetBeats = index < pickupIndex ? beforeBeats : afterBeats
    const maxStep = endIndex - index - (afterBeats * remainingSlides)
    let step = Math.min(targetBeats, maxStep)
    if (index < pickupIndex && index + step > pickupIndex) {
      step = pickupIndex - index
    }
    if (step <= 0) {
      step = Math.max(1, maxStep)
    }
    index += step
    cuts.push(TRACK_ZERO_BEAT_TIMES[Math.min(index, endIndex)] ?? endTime)
  }

  cuts[cuts.length - 1] = endTime
  return cuts
}
